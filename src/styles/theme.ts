// Utility functions
export function getBreakpointValue(breakpoint: keyof typeof BREAKPOINTS): number {
  return parseInt(BREAKPOINTS[breakpoint]);
}

export function pxToRem(px: number): string {
  return `${px / 16}rem`;
}

export function remToPx(rem: string): number {
  return parseFloat(rem) * 16;
}

export function createTransition(
  properties: string[],
  duration: keyof typeof TRANSITIONS = 'normal',
  timing: keyof typeof TRANSITIONS.timing = 'ease'
): string {
  return properties
    .map((property) => `${property} ${TRANSITIONS[duration]} ${TRANSITIONS.timing[timing]}`)
    .join(', ');
}
